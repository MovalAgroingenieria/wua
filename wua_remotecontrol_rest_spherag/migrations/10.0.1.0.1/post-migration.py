from odoo import api, SUPERUSER_ID


def migrate(cr, version):
    env = api.Environment(cr, SUPERUSER_ID, {})
    values = env['ir.values'].sudo()
    values.set_default(
        'wua.irrigation.configuration',
        'import_from_waterconnection_spherag',
        1,
    )
    return
